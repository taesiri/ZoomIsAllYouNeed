def benchmark_masked(
    model, zeroshot_weights, dataset_folder, img_transform, all_wnids, bs=32
):
    predictions = []
    confidences = []

    dataset = ImageFolder(root=dataset_folder, transform=img_transform)
    test_loader = DataLoader(
        dataset, batch_size=bs, shuffle=False, num_workers=8, pin_memory=True
    )

    dataset_wnids = set([x[0].split("/")[-2] for x in test_loader.dataset.imgs])
    mask = [wnid in set(dataset_wnids) for wnid in all_wnids]

    correct_ones = 0
    with torch.inference_mode():
        for _, (data, target) in enumerate(tqdm(test_loader)):
            data = data.cuda()
            target = target.cuda()

            # TODO UPDATE ME
            image_features = model.encode_image(data)
            image_features /= image_features.norm(dim=-1, keepdim=True)
            logits = 100.0 * image_features @ zeroshot_weights
            output = logits[:, mask]

            pred = output.argmax(dim=1, keepdim=True)
            correct_ones += pred.eq(target.view_as(pred)).sum().item()
            probs = torch.softmax(output, dim=1)

            predictions.extend(pred.data.cpu().numpy())
            confidences.extend(probs.data.cpu().numpy())

    # Free-up the gpu memory inside torch
    del data
    del target
    torch.cuda.empty_cache()

    return 100 * correct_ones / len(dataset), predictions, confidences


