def run(model_name, VAL_FOLDER, OUTPUT, slice_x, slice_y, output_dir):
    all_wnids = list(sorted(os.listdir(IMAGENET_TRAIN_FOLDER)))

    list_of_tsizes = generate_transform_size_list()
    confidences = {}
    predictions = {}
    accuracies = {}

    model, _ = clip.load(model_name)
    model.cuda()
    model.eval()

    zeroshot_weights = zeroshot_classifier(model, imagenet_classes, imagenet_templates)

    for tsize in tqdm(list_of_tsizes):
        current_transform = transforms.Compose(
            [
                transforms.Resize(
                    tsize,
                    interpolation=torchvision.transforms.InterpolationMode.BICUBIC,
                    max_size=None,
                    antialias=None,
                ),
                crop_at(224, slice_x, slice_y),
                transforms.ToTensor(),
                transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
            ]
        )

        acc, preds, confs = benchmark_masked(
            model,
            zeroshot_weights,
            dataset_folder=VAL_FOLDER,
            img_transform=current_transform,
            all_wnids=all_wnids,
        )
        accuracies[tsize] = acc
        confidences[tsize] = confs
        predictions[tsize] = preds

    x_ = np.asarray(list(sorted(list(accuracies.keys()))))
    y_ = np.asarray(list([accuracies[l] for l in x_]))

    for folder_name in ("plots", "predictions", "accuracies", "confidences"):
        if not os.path.exists(f"./{output_dir}/{folder_name}"):
            os.mkdir(f"./{output_dir}/{folder_name}")

    with open(
        f"./{output_dir}/accuracies/{OUTPUT}_{slice_x}_{slice_y}.pickle",
        "wb",
    ) as f:
        pickle.dump(accuracies, f)

    with open(
        f"./{output_dir}/predictions/{OUTPUT}_{slice_x}_{slice_y}.pickle",
        "wb",
    ) as f:
        pickle.dump(predictions, f)

    with open(
        f"./{output_dir}/confidences/{OUTPUT}_{slice_x}_{slice_y}.pickle",
        "wb",
    ) as f:
        pickle.dump(confidences, f)


def zeroshot_classifier(model, classnames, templates):
    with torch.no_grad():
        zeroshot_weights = []
        for classname in tqdm(classnames):
            texts = [template.format(classname) for template in templates]
            texts = clip.tokenize(texts).cuda()
            class_embeddings = model.encode_text(texts)
            class_embeddings /= class_embeddings.norm(dim=-1, keepdim=True)

            class_embedding = class_embeddings.mean(dim=0)

            class_embedding /= class_embedding.norm()
            zeroshot_weights.append(class_embedding)
        zeroshot_weights = torch.stack(zeroshot_weights, dim=1).cuda()
    return zeroshot_weights


